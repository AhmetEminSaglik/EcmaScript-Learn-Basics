let myNumber = 32;
console.log("myNumber : " + myNumber); // returns  Decimal
console.log("(decimal) myNumber.toString(10) : " + myNumber.toString(10)); // returns  Decimal
console.log("(octal) myNumber.toString(8) : " + myNumber.toString(8));  // returns  Octal
console.log("(binary) myNumber.toString(2) : " + myNumber.toString(2));   // returns  Binary
/*  Method        	Description
toString()	       Returns a number as a string
toExponential()	   Returns a number written in exponential notation
toFixed()	       Returns a number written with a number of decimals
toPrecision()	   Returns a number written with a specified length
ValueOf()	       Returns a number as a number
*/